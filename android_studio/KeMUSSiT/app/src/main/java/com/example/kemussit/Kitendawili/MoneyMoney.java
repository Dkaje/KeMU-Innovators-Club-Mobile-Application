package com.example.kemussit.Kitendawili;

public class MoneyMoney {
    String fresh = null;
    String renewal = null;
    String upgrade = null;
    String contribute = null;
    String event_pay=null;
    String withdraw = null;
    String whole = null;
    String bal = null;
    String entry = null;
    String last = null;

    public MoneyMoney(String fresh, String renewal, String upgrade, String contribute, String event_pay, String withdraw, String whole, String bal, String entry, String last) {
        this.fresh = fresh;
        this.renewal = renewal;
        this.upgrade = upgrade;
        this.contribute = contribute;
        this.event_pay = event_pay;
        this.withdraw = withdraw;
        this.whole = whole;
        this.bal = bal;
        this.entry = entry;
        this.last = last;
    }

    public String getFresh() {
        return fresh;
    }

    public void setFresh(String fresh) {
        this.fresh = fresh;
    }

    public String getRenewal() {
        return renewal;
    }

    public void setRenewal(String renewal) {
        this.renewal = renewal;
    }

    public String getUpgrade() {
        return upgrade;
    }

    public void setUpgrade(String upgrade) {
        this.upgrade = upgrade;
    }

    public String getContribute() {
        return contribute;
    }

    public void setContribute(String contribute) {
        this.contribute = contribute;
    }

    public String getEvent_pay() {
        return event_pay;
    }

    public void setEvent_pay(String event_pay) {
        this.event_pay = event_pay;
    }

    public String getWithdraw() {
        return withdraw;
    }

    public void setWithdraw(String withdraw) {
        this.withdraw = withdraw;
    }

    public String getWhole() {
        return whole;
    }

    public void setWhole(String whole) {
        this.whole = whole;
    }

    public String getBal() {
        return bal;
    }

    public void setBal(String bal) {
        this.bal = bal;
    }

    public String getEntry() {
        return entry;
    }

    public void setEntry(String entry) {
        this.entry = entry;
    }

    public String getLast() {
        return last;
    }

    public void setLast(String last) {
        this.last = last;
    }
}
